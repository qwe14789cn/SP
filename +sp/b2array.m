%--------------------------------------------------------------------------
%   [bin_array] = b2array(str)
%--------------------------------------------------------------------------
%   功能:
%   将二进制符号转换为二进制数组
%--------------------------------------------------------------------------
%   输入:
%           str                     二进制char 或者string
%   output:
%           bin_array               二进制数 数组形式
%--------------------------------------------------------------------------
%   例子:
%   b2array('1101')
%   ans =
%       1×4 logical 数组
%       1   1   0   1
%--------------------------------------------------------------------------
function bin_array = b2array(str)
for idx = 1:size(str,1)
    bin_array(idx,:) = logical(str2num(char(str(idx,:))')');
end
end
