%--------------------------------------------------------------------------
%   cache_array(array,new)
%--------------------------------------------------------------------------
%   功能：
%   队列缓冲区
%--------------------------------------------------------------------------
%   输入:
%           array           队列变量
%           new             新变量
%   输出：
%           out             输出队列
%--------------------------------------------------------------------------
%   例子:
%   array = cache_array(array,new)
%--------------------------------------------------------------------------
function out = cache_array(array,new)
shape = size(array);
array = array(:);
out = [array(2:end);new];
out = reshape(out,shape);
end
