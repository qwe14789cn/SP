%--------------------------------------------------------------------------
%   [sig,T_axis] = exp_wave(T,fp,fs,fai)
%--------------------------------------------------------------------------
%   功能:
%   复数点频信号生成器
%--------------------------------------------------------------------------
%   输入:
%           T                   信号持续时间
%           fp                  信号频率
%           fs                  采样频率
%           fai                 初始相位
%   输出:
%           sig                 生成复数点频信号
%           T_axis              时间轴
%--------------------------------------------------------------------------
%   例子:   
%   exp_wave(1e-5,5e6,50e6)
%   exp_wave(1e-5,5e6,50e6,pi/2)
%--------------------------------------------------------------------------
function [sig,t] = exp_wave(T,fp,fs,fai)
if nargin==3
    fai = 0;
end
t = (0:1/fs:T-1/fs)';
sig = exp(1j*(2*pi*fp*t+fai));
end
