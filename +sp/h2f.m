%--------------------------------------------------------------------------
%   [num] = h2f(hex)
%--------------------------------------------------------------------------
%   功能:
%   十六进制值转换为浮点值
%--------------------------------------------------------------------------
%   输入:
%           hex                     十六进制数
%   output:
%           num                     浮点数
%--------------------------------------------------------------------------
%   例子:
%  sp.h2f(["bf800000" "c0000000";"c0400000"    "c0800000"])
% ans =
%     -1    -2
%     -3    -4
%--------------------------------------------------------------------------
function num = h2f(hex)
hex = string(hex);
q = quantizer('mode','single','format',[32 8]);
if size(hex,1) == 1
    num = hex2num(q,hex);
else
    num = cell2mat(reshape(hex2num(q,hex),size(hex)));
end