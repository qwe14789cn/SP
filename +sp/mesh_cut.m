%--------------------------------------------------------------------------
%   mesh_cut(img,cut_N)
%--------------------------------------------------------------------------
%   功能：
%   切边图像mesh
%--------------------------------------------------------------------------
%   输入:
%           img         原始图像
%           cut_N       切除边缘行列数
%--------------------------------------------------------------------------
%   例子:
%   cut_mesh(img,cut_N)
%--------------------------------------------------------------------------
function mesh_cut(img,cut_N)
img = img(cut_N+1:end-cut_N,cut_N+1:end-cut_N);
mesh(img);
end