%--------------------------------------------------------------------------
%   [vpp] = dbm2w(dbm)
%--------------------------------------------------------------------------
%   功能:
%   分贝毫瓦dbm 转化为瓦特W 
%--------------------------------------------------------------------------
%   输入:
%           dbm                 signal peak to peak volt
%   输出:
%           W
%--------------------------------------------------------------------------
%   例子:   
%   dBm2w(10)
%--------------------------------------------------------------------------
function Pw = dbm2w(dbm)
Pw = 10^((dbm-30)/10);
end