%--------------------------------------------------------------------------
%   [dataout] = d2d(datain,N_bit)
%--------------------------------------------------------------------------
%   功能:
%   转换无符号10进制数到有符号10进制数(常用于FPGA数据测试)
%--------------------------------------------------------------------------
%   输入:
%           datain                  输入10进制数
%           N_bit                   转换位数
%   输出:
%           dataout                 输出10进制数
%--------------------------------------------------------------------------
%   例子:   
% sp.d2d(9688250,24)
% ans =
%     -7088966
%--------------------------------------------------------------------------
function dataout = d2d(datain,N_bit)
index = datain>=2^(N_bit-1);
dataout = datain;
dataout(index) = datain(index) - 2^N_bit;