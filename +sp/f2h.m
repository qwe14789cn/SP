%--------------------------------------------------------------------------
%   [hex] = f2h(num)
%--------------------------------------------------------------------------
%   功能:
%   浮点型转换为十六进制值
%--------------------------------------------------------------------------
%   输入:
%           num                     浮点数
%   output:
%           hex                     十六进制数
%--------------------------------------------------------------------------
%   例子:
% sp.f2h([-1 -2;-3 -4])
% 
% ans = 
% 
%   2×2 string 数组
% 
%     "bf800000"    "c0000000"
%     "c0400000"    "c0800000"
%--------------------------------------------------------------------------
function hex = f2h(num)
num = double(num);
q = quantizer('mode','single','format',[32 8]);
hex = reshape(string(num2hex(q,num)),size(num));
end