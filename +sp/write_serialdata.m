%--------------------------------------------------------------------------
%   d = write_serialdata(comname,rate,data)
%--------------------------------------------------------------------------
%   功能：
%   串口数据写入
%--------------------------------------------------------------------------
%   输入:
%           comname         串口号
%           rate            波特率
%           data            写入uint8数据
%--------------------------------------------------------------------------
%   例子:
%   data = write_serialdata("COM4",115200,[1 2 3 4 5])
%--------------------------------------------------------------------------
function write_serialdata(comname,rate,data)
s = serialport(comname,rate);
write(s,data,'uint8')
clear s
end