%--------------------------------------------------------------------------
%   [sig,T_axis] = zeros_wave(T,fs)
%--------------------------------------------------------------------------
%   功能:
%   复数点频信号生成器
%--------------------------------------------------------------------------
%   输入:
%           T                   信号持续时间
%           fs                  采样频率
%   输出:
%           sig                 生成复数点频信号
%           T_axis              时间轴
%--------------------------------------------------------------------------
%   例子:   
%   zeros_wave(1e-5,50e6)
%--------------------------------------------------------------------------
function [sig,t] = zeros_wave(T,fs)
t = (0:1/fs:T-1/fs)';
sig = zeros(size(t));
end
