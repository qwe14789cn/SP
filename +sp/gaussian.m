%--------------------------------------------------------------------------
%   g = gaussian(x,pos,wid)
%--------------------------------------------------------------------------
%   功能：
%   高斯波形发生器
%--------------------------------------------------------------------------
%   输入:
%           x           坐标轴
%           pos         波形中心点
%           wid         高斯波形宽度
%	输出：
%           g           回波信号
%--------------------------------------------------------------------------
%   例子:
%   g = gaussian(1:1000,100,50)
%--------------------------------------------------------------------------
function g = gaussian(x,pos,wid)
g = exp(-((x-pos)./(0.60056120439323.*wid)) .^2);
g = g(:);
end