%--------------------------------------------------------------------------
%   wave = nlfm_square_law_wave(T,B,fs)
%--------------------------------------------------------------------------
%   功能：
%   非线性调频:平方律调频信号生成工具
%--------------------------------------------------------------------------
%   输入：
%           T                   信号持续时间
%           B                   信号带宽
%           fs                  信号采样速率
%   输出：
%           wave                宽带信号波形
%--------------------------------------------------------------------------
%   例子：
%   sig = nlfm_square_law_wave(1e-4,50e6,100e6)
%--------------------------------------------------------------------------
function wave = nlfm_square_law_wave(T,B,fs)
delta_T = 1/fs;
t_axis = (-T/2:delta_T:T/2-delta_T)';
u = 2*pi*B/(T/2)^2;
wave = exp(1j.*1/3*u.*t_axis.^3);
end