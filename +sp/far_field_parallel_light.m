%--------------------------------------------------------------------------
%   far_field_parallel_light(D)
%--------------------------------------------------------------------------
%   功能：
%   光学镜头系统远场假设
%--------------------------------------------------------------------------
%   输入:
%           D           镜头孔径
%   输出:
%           R           点光源最小距离
%--------------------------------------------------------------------------
%   例子:
%   R = far_field_parallel_light(25e-3)
%--------------------------------------------------------------------------
function R = far_field_parallel_light(D)
R = D/2/tand(0.1);                                                          %假设0.1度就近似为平行光，经验公式
disp(['镜头直径:' num2str(D) ' m 远场距离至少:' num2str(R) ' m'])
end