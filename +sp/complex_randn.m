%--------------------------------------------------------------------------
%   output = complex_randn(in,in2)
%--------------------------------------------------------------------------
%   功能：
%   复噪声高斯分布随机生成器,单位为1
%--------------------------------------------------------------------------
%   输入：
%           in          方阵长度
%           in2         矩阵长度
%--------------------------------------------------------------------------
%   例子：
%   complex_randn(5)
%   complex_randn(10,20)
%--------------------------------------------------------------------------
function output = complex_randn(in,in2)
if nargin ==1
    output = 1./sqrt(2)*(randn(in) + 1j.*randn(in));
else
    output = 1./sqrt(2)*(randn(in,in2) + 1j.*randn(in,in2));
end
    
end