%--------------------------------------------------------------------------
%   [num] = b2f(bin)
%--------------------------------------------------------------------------
%   功能:
%   二进制值转换为浮点值
%--------------------------------------------------------------------------
%   输入:
%           bin                     二进制数
%   output:
%           num                     浮点数
%--------------------------------------------------------------------------
%   例子:
% a = 
%   2×2 string 数组
%     "10111111100000000000000000000000"    "11000000000000000000000000000000"
%     "11000000010000000000000000000000"    "11000000100000000000000000000000"
%     sp.b2f(a)
% ans =
%     -1    -2
%     -3    -4
%--------------------------------------------------------------------------
function num = b2f(bin)
q = quantizer('mode','single','format',[32 8]);
if numel(bin) == 1
    num = reshape(bin2num(q,bin),size(bin));
else
    num = cell2mat(reshape(bin2num(q,bin),size(bin)));
end