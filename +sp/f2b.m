%--------------------------------------------------------------------------
%   [bin] = f2b(num)
%--------------------------------------------------------------------------
%   功能:
%   浮点型转换为二进制值
%--------------------------------------------------------------------------
%   输入:
%           num                     浮点数
%   output:
%           bin                     二进制数
%--------------------------------------------------------------------------
%   例子:
% sp.f2b([-1 -2;-3 -4])
% 
% ans = 
% 
%   2×2 string 数组
% 
%     "10111111100000000000000000000000"    "11000000000000000000000000000000"
%     "11000000010000000000000000000000"    "11000000100000000000000000000000"

%--------------------------------------------------------------------------
function bin = f2b(num)
q = quantizer('mode','single','format',[32 8]);
bin = reshape(string(num2bin(q,num)),size(num));
end